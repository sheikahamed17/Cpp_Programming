#include<iostream>
using namespace std;

int main()
{
    int a =10,b=7;
    cout<<"a++ = "<<a++<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b-- = "<<b--<<endl;
    cout<<"b = "<<b<<endl;
    cout<<"++a = "<<++a<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"--b = "<<--b<<endl;
    cout<<"b = "<<b<<endl;
}

