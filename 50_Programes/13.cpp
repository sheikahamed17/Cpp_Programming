#include<iostream>
using namespace std;

int main()
{
    int a = 10,b = 20;
    cout<<"A+B(Addition) = "<<a+b<<endl;
    cout<<"A-B(Subraction) = "<<a-b<<endl;
    cout<<"A*B(Multiplication) = "<<a*b<<endl;
    cout<<"A/B(Division) = "<<a/b<<endl;
    cout<<"A%B(Modulus) = "<<a%b<<endl;
    return 0;
}
